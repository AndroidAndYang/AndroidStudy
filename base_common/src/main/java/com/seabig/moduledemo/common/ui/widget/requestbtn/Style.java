public enum Style {
    /**
     * tick when start circle
     * <p>
     * 开始画圆就开始打钩
     */
    TICK_START_CIRCLE,
    /**
     * tick when half circle
     * <p>
     * 圆画到一半开始打钩
     */
    TICK_HALF_CIRCLE,
    /**
     * tick when circle finished
     * <p>
     * 画完圆开始打钩
     */
    TICK_END_CIRCLE
}
